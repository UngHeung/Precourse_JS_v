class TestPr1 {
    constructor(solution) {
        this.solution = solution;
        this.checkScore();
    }

    setResult = (result) => {
        this.result = result;
    };

    printResult = () => {
        return this.result;
    };

    /** 점수 확인 */
    checkScore = () => {
        // result case 1, 2, 3
        const result = [0, 1, -1];
        const [pobi, crong] = [
            [
                // pobi test case 1, 2, 3
                [97, 98],
                [131, 132],
                [99, 102],
            ],
            [
                // crong test case 1, 2, 3
                [197, 198],
                [211, 212],
                [211, 212],
            ],
        ];

        let count = 0;

        result.map((_, i) => (this.solution(pobi[i], crong[i]) === result[i] ? count++ : 0));

        let score = ~~((count / result.length) * 100);

        this.setResult(
            `
                테스트 케이스 개수: ${result.length}<br>
                맞춘 개수: ${count}<br>
                점수: ${score}점<br>
            `
        );
    };
}

